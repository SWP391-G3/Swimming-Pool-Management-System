package model.manager;

/**
 *
 * @author Tuan Anh
 */
public class PoolFeedBack {
    
    private int id;
    private String name;

    public PoolFeedBack() {
    }

    public PoolFeedBack(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
    
    
    
    
}
